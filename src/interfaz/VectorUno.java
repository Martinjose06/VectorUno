    private void cmdCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCrearActionPerformed

        int L;

        if (txtL.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite La Longitud", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtL.requestFocusInWindow();
        } else if (Integer.parseInt(txtL.getText().trim()) == 0){
            JOptionPane.showMessageDialog(this, "Digite Una Longitud Superior A 0", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtL.requestFocusInWindow();
            txtL.selectAll();
        } else {

            L = Integer.parseInt(txtL.getText());
            v = new double[L];
            JOptionPane.showMessageDialog(this, "Vector Creado Satisfatoriamente");
            txtL.setText("");

            cmdCrear.setEnabled(false);
            cmdLlenadoM.setEnabled(true);
            cmdLlenadoA.setEnabled(true);
            cmdMostrarM.setEnabled(false);
            cmdMostrarTodo.setEnabled(false);
            cmdBorrar.setEnabled(true);
            txtL.setEditable(false);

        }

    }//GEN-LAST:event_cmdCrearActionPerformed

    private void cmdLlenadoMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdLlenadoMActionPerformed

        double n;
        acum = 0;
        mul = 1;

        
        for (int i = 0; i < v.length; i++) {
            
        
            n = Double.parseDouble(JOptionPane.showInputDialog("Digite el elemento de la posicion " + i));
            
            v[i] = n;
            acum = acum + v[i];
            mul = mul * v[i];

        }
        JOptionPane.showMessageDialog(this, "Vector Llenado Con Exito");

        for (int i = 0; i < v.length; i++) {

            if (i == 0) {

                may = v[0];
                men = v[0];

            }
            if (v[i] > may) {
                may = v[i];
            }
            if (v[i] < men) {
                men = v[i];
            }

        }

        cmdCrear.setEnabled(false);
        cmdLlenadoM.setEnabled(false);
        cmdLlenadoA.setEnabled(false);
        cmdMostrarM.setEnabled(true);
        cmdMostrarTodo.setEnabled(true);
        cmdBorrar.setEnabled(true);
        txtL.setEditable(false);

    }//GEN-LAST:event_cmdLlenadoMActionPerformed

    private void cmdLlenadoAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdLlenadoAActionPerformed

        double n;

        acum = 0;
        mul = 1;
        for (int i = 0; i < v.length; i++) {
            n = (int) (Math.random() * 25 + 1);
            v[i] = n;
            acum = acum + v[i];
            mul = mul * v[i];

        }

        for (int i = 0; i < v.length; i++) {
            if (i == 0) {

                may = v[0];
                men = v[0];

            }
            if (v[i] > may) {
                may = v[i];
            }
            if (v[i] < men) {
                men = v[i];
            }

        }

        JOptionPane.showMessageDialog(this, "Vector Llenado Con Exito");

        cmdCrear.setEnabled(false);
        cmdLlenadoM.setEnabled(false);
        cmdLlenadoA.setEnabled(false);
        cmdMostrarM.setEnabled(true);
        cmdMostrarTodo.setEnabled(true);
        cmdBorrar.setEnabled(true);
        txtL.setEditable(false);
    }//GEN-LAST:event_cmdLlenadoAActionPerformed

